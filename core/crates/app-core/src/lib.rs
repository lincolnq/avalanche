    /// Fetch public metadata for an account: display name and bot flag.
    ///
    /// Call from a background thread — this blocks until complete.
    pub fn get_account_info(&self, did: String) -> Result<AccountInfoFfi, AppErrorFfi> {
        ffi_runtime().block_on(async {
            let inner = self.inner.lock().await;

            // Map a cached account record to the FFI shape.
            let from_cache = |c: store::profiles::AccountInfoCache| AccountInfoFfi {
                did: c.did,
                display_name: if c.display_name.is_empty() { None } else { Some(c.display_name) },
                is_bot: c.is_bot,
            };

            // Throttle: skip the server hit when a recent attempt is still
            // inside its skip window (docs/52). Serve the cached record if we
            // have one; otherwise report the throttled miss so the caller falls
            // back to a placeholder without re-hitting the server every render.
            if !inner.should_fetch_name(&did).await {
                return match inner.store.load_account_info(&did).await.map_err(AppError::from)? {
                    Some(c) => Ok::<_, AppError>(from_cache(c)),
                    None => Err(AppError::Protocol("account info fetch throttled".into())),
                };
            }

            // Server is authoritative; on success write through to the local
            // cache so the name + bot flag survive offline (bot names have no
            // other local source — see store `account_info_cache`).
            match inner.client.get_account_info(&did).await {
                Ok(info) => {
                    inner.record_fetch(&did, crate::messaging::FetchOutcome::Success).await;
                    // Only cache rows that carry signal — a real name or the
                    // bot flag. An empty human record becomes a throttle
                    // outcome, not a junk cache row (docs/52 §"Negative-row
                    // hygiene").
                    if info.is_bot || info.display_name.is_some() {
                        let _ = inner.store.upsert_account_info(&store::profiles::AccountInfoCache {
                            did: info.did.clone(),
                            display_name: info.display_name.clone().unwrap_or_default(),
                            is_bot: info.is_bot,
                            fetched_at: Timestamp::now(),
                        }).await;
                    }
                    Ok(AccountInfoFfi {
                        did: info.did,
                        display_name: info.display_name,
                        is_bot: info.is_bot,
                    })
                }
                // Offline / fetch failure: record the outcome (so 404s get the
                // 6h negative-cache window, transport errors retry in 1m), then
                // fall back to the cached record so bot DM titles + avatars
                // still resolve. Propagate the error only with no cached row.
                Err(e) => {
                    inner.record_fetch(&did, crate::messaging::classify_net_error(&e)).await;
                    match inner.store.load_account_info(&did).await.map_err(AppError::from)? {
                        Some(c) => Ok(from_cache(c)),
                        None => Err(AppError::from(e)),
                    }
                }
            }
        }).map_err(AppErrorFfi::from)
    }

    /// Register / refresh the push registration for this device.
    ///
    /// Always re-uploads the current `(pseudonym, device_token, platform,
    /// environment)` to the relay and the pseudonym to the homeserver —
    /// both endpoints accept idempotent re-registers, and re-uploading on
    /// every launch is the easiest way to recover from changes the client
    /// can't detect locally (relay URL change, relay losing its SQLite,
    /// device_token churn, etc.).
    ///
    /// Rotates the pseudonym (and unregisters the old one) when the
    /// existing registration is older than [`PUSH_ROTATION_MAX_AGE_SECONDS`]
    /// or its `(device_token, platform)` has changed.
    ///
    /// - `platform`: `"apns"` (iOS) or `"fcm"` (Android).
    /// - `relay_url`: base URL of the push relay.
    /// - `environment`: `"sandbox"` (debug builds) or `"production"` (App Store/TestFlight).
    ///
    /// Call from a background thread — this blocks until complete.
    pub fn register_push_token(
        &self,
        device_token: String,
        platform: String,
        relay_url: String,
        environment: String,
    ) -> Result<(), AppErrorFfi> {
        ffi_runtime().block_on(async {
            let inner = self.inner.lock().await;
            let existing = inner.store.load_push_state().await.map_err(AppError::Store)?;
            let now = std::time::SystemTime::now()
                .duration_since(std::time::UNIX_EPOCH)
                .map(|d| d.as_secs() as i64)
                .unwrap_or(0);

            let rotate = match &existing {
                None => true,
                Some(s) => {
                    s.device_token != device_token
                        || s.platform != platform
                        || now - s.registered_at >= PUSH_ROTATION_MAX_AGE_SECONDS
                }
            };

            let pseudonym = if rotate {
                let bytes: [u8; 32] = rand::Rng::random(&mut rand::rng());
                let new_pseudonym = base64::prelude::BASE64_URL_SAFE_NO_PAD.encode(bytes);
                inner.store.save_push_state(store::push::PushState {
                    pseudonym: new_pseudonym.clone(),
                    device_token: device_token.clone(),
                    platform: platform.clone(),
                    registered_at: 0, // ignored by save
                }).await.map_err(AppError::Store)?;
                new_pseudonym
            } else {
                existing.as_ref().unwrap().pseudonym.clone()
            };

            // Always re-register — relay+server treat this as INSERT OR REPLACE.
            inner.client
                .register_push_with_relay(&relay_url, &pseudonym, &device_token, &platform, &environment)
                .await.map_err(AppError::Net)?;
            inner.client.register_push_pseudonym(&pseudonym).await
                .map_err(AppError::Net)?;

            // If we rotated, retire the previous pseudonym. Best-effort —
            // a failure leaves a stranded row that the relay's GC reaps.
            if rotate {
                if let Some(old) = existing {
                    let _ = inner.client.unregister_push_with_relay(&relay_url, &old.pseudonym).await;
                    let _ = inner.client.unregister_push_pseudonym(&old.pseudonym).await;
                }
            }

            Ok::<_, AppError>(())
        }).map_err(AppErrorFfi::from)
    }

    /// Update the recovery blob on the server (e.g. after joining a new server).
    /// `prf_output` is the 32-byte WebAuthn PRF output; the blob-encryption
    /// key is derived from it via HKDF.
    /// `servers` is the updated list of homeserver URLs.
    pub fn update_recovery_blob(
        &self,
        prf_output: Vec<u8>,
        servers: Vec<String>,
    ) -> Result<(), AppErrorFfi> {
        ffi_runtime().block_on(async {
            if prf_output.len() != 32 {
                return Err(AppError::Protocol("prf_output must be 32 bytes".into()));
            }
            let (_, key) = recovery::derive_recovery_keys_from_prf(&prf_output);

            let inner = self.inner.lock().await;
            let identity = inner.store.load_identity().await?
                .ok_or(AppError::NoAccount)?;
            let own_profile = inner.store.load_own_profile().await?;
            let groups = recovery::collect_group_blob_entries(&inner.store).await?;
            let storage_key = inner.store.load_storage_key().await?;

            let plaintext = recovery::build_recovery_blob(
                &identity.serialize(),
                &servers,
                own_profile.as_ref().map(|p| p.profile_key.as_slice()).unwrap_or(&[]),
                own_profile.as_ref().map(|p| p.display_name.as_str()).unwrap_or(""),
                &groups,
                storage_key.as_ref().map(|k| k.as_slice()).unwrap_or(&[]),
            );
            let blob = recovery::encrypt_recovery_blob(&plaintext, &key)?;
            inner.client.update_recovery_blob(&blob).await?;
            Ok::<_, AppError>(())
        }).map_err(AppErrorFfi::from)
    }

