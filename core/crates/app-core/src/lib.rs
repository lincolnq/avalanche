    /// Send an encrypted DM to a recipient. Plaintext is wrapped in a
    /// content envelope (with the sender's timestamp) before encryption.
    /// Internally fans out one ciphertext per active recipient device.
    ///
    /// Call from a background thread — this blocks until complete.
    pub fn send_dm(
        &self,
        recipient_did: String,
        plaintext: Vec<u8>,
        sent_at_ms: i64,
    ) -> Result<(), AppErrorFfi> {
        ffi_runtime().block_on(async {
            let ws = self.ws.lock().expect("ws mutex poisoned").clone();
            let mut inner = self.inner.lock().await;
            // Refuse outgoing messages to a blocked contact (docs/12 §2).
            inner.ensure_not_blocked(&recipient_did).await?;
            let body = String::from_utf8_lossy(&plaintext).into_owned();
            let profile_key = inner.own_profile_key().await;
            let expire_timer_secs = inner.dm_expire_timer(&recipient_did).await;
            let msg = ContentMessage {
                body: Some(Body::Text(TextMessage { body })),
                timestamp_ms: sent_at_ms as u64,
                profile_key,
                expire_timer_secs,
            };
            inner.send_dm(ws.as_ref(), &recipient_did, &msg.encode_to_vec(), None).await?;
            // Sending a DM is a deliberate gesture (docs/35 §"What changes a row").
            let _ = inner
                .store
                .touch_contact(&recipient_did, true, Timestamp::now())
                .await;
            Ok::<_, AppError>(())
        }).map_err(AppErrorFfi::from)
    }

    /// Send a plain-text message to a [`MessageTarget`] — a DM or a group —
    /// without the caller having to pick the right transport-specific entry
    /// point. The DM and group sends differ only in their conversation key and
    /// wire transport; this folds that fork into one call (the same way
    /// `send_reaction` / `send_edit` / `send_delete` already do via
    /// [`AppCoreInner::send_to_target`]).
    ///
    /// Behaviour matches the targeted primitive exactly: a `Dm` target wraps
    /// the body in a content envelope, fans out per recipient device, and marks
    /// the recipient a curated contact (the deliberate-gesture rule, docs/35); a
    /// `Group` target encrypts under our Sender Key and fans out to members.
    ///
    /// Call from a background thread — this blocks until complete.
    pub fn send_message(
        &self,
        target: MessageTarget,
        plaintext: Vec<u8>,
        sent_at_ms: i64,
    ) -> Result<(), AppErrorFfi> {
        ffi_runtime().block_on(async {
            let ws = self.ws.lock().expect("ws mutex poisoned").clone();
            let mut inner = self.inner.lock().await;
            // Refuse outgoing DMs to a blocked contact (docs/12 §2). Group
            // sends are unaffected — blocking is a per-DID DM remedy.
            if let MessageTarget::Dm { recipient_did } = &target {
                inner.ensure_not_blocked(recipient_did).await?;
            }
            let body = String::from_utf8_lossy(&plaintext).into_owned();
            inner
                .send_to_target(
                    ws.as_ref(),
                    &target,
                    Body::Text(TextMessage { body }),
                    sent_at_ms as u64,
                )
                .await?;
            if let MessageTarget::Dm { recipient_did } = &target {
                let _ = inner
                    .store
                    .touch_contact(recipient_did, true, Timestamp::now())
                    .await;
            }
            Ok::<_, AppError>(())
        }).map_err(AppErrorFfi::from)
    }

