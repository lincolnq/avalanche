    /// Re-fetch a contact's encrypted profile and update the cache if the
    /// decrypted name changed. Call this when the user opens a conversation
    /// — it's the primary change-detection path.
    ///
    /// Returns true if the cached display name was updated.
    pub fn refresh_contact_profile(&self, did: String) -> Result<bool, AppErrorFfi> {
        ffi_runtime().block_on(async {
            let inner = self.inner.lock().await;
            let cached = inner.store.load_contact_profile(&did).await
                .map_err(AppError::from)?;
            let cached = match cached {
                Some(c) if c.profile_key.len() == profile::PROFILE_KEY_LEN => c,
                _ => return Ok::<_, AppError>(false),
            };

            // Throttle the opportunistic conversation-open refetch (docs/52).
            // Called on every onAppear; skip when a recent attempt is still
            // inside its window. The version-mismatch path (inbound) is the
            // unthrottled change-driven trigger and lives elsewhere.
            if !inner.should_fetch_name(&did).await {
                return Ok(false);
            }

            let blob = match inner.client.get_profile(&did).await {
                Ok(Some(b)) => {
                    inner.record_fetch(&did, crate::messaging::FetchOutcome::Success).await;
                    b
                }
                // No blob on the server — negative-cache it (6h) so we stop
                // asking until something changes.
                Ok(None) => {
                    inner.record_fetch(&did, crate::messaging::FetchOutcome::NotFound).await;
                    return Ok(false);
                }
                Err(e) => {
                    inner.record_fetch(&did, crate::messaging::classify_net_error(&e)).await;
                    return Err(AppError::from(e));
                }
            };

            let mut key = [0u8; profile::PROFILE_KEY_LEN];
            key.copy_from_slice(&cached.profile_key);
            let plaintext = match profile::decrypt_profile(&blob, &key) {
                Ok(p) => p,
                Err(_) => return Ok(false),
            };

            let changed = plaintext.display_name != cached.display_name;
            inner.store.upsert_contact_profile(&store::profiles::ContactProfile {
                did,
                display_name: plaintext.display_name,
                profile_key: cached.profile_key,
                fetched_at: Timestamp::now(),
            }).await.map_err(AppError::from)?;
            Ok(changed)
        }).map_err(AppErrorFfi::from)
    }

    /// Enumerate every known contact, newest interaction first. Each row
    /// joins the curation flag from the `contacts` table with the cached
    /// display name from `contact_profiles` when available. The compose
    /// autocomplete shows `is_curated == true` rows under "People" and
    /// the rest under "Other".
    pub fn list_contacts(&self) -> Result<Vec<ContactRowFfi>, AppErrorFfi> {
        ffi_runtime().block_on(async {
            let inner = self.inner.lock().await;
            let rows = inner.store.list_contacts().await.map_err(AppError::from)?;
            let own_did = inner.did.clone();
            let mut out = Vec::with_capacity(rows.len());
            for row in rows {
                // Don't show ourselves in our own contact list.
                if row.did == own_did {
                    continue;
                }
                // Name source priority mirrors `resolveDisplayName` on the
                // client: the encrypted-profile name (humans) first, then the
                // cached server account record (bots). Both are local reads, so
                // contact-book names — including bots — resolve offline.
                let mut display_name = inner
                    .store
                    .load_contact_profile(&row.did)
                    .await
                    .ok()
                    .flatten()
                    .map(|p| p.display_name)
                    .unwrap_or_default();
                if display_name.is_empty() {
                    display_name = inner
                        .store
                        .load_account_info(&row.did)
                        .await
                        .ok()
                        .flatten()
                        .map(|c| c.display_name)
                        .unwrap_or_default();
                }
                out.push(ContactRowFfi {
                    did: row.did,
                    display_name,
                    is_curated: row.is_curated,
                    last_interaction_at_ms: row.last_interaction_at.as_millis(),
                });
            }
            Ok::<_, AppError>(out)
        })
        .map_err(AppErrorFfi::from)
    }

