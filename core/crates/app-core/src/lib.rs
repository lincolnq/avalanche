    /// Send an encrypted DM to a recipient. Plaintext is wrapped in a
    /// content envelope (with the sender's timestamp) before encryption.
    /// Internally fans out one ciphertext per active recipient device.
    ///
    /// Call from a background thread — this blocks until complete.
    pub fn send_dm(
        &self,
        recipient_did: String,
        plaintext: Vec<u8>,
        sent_at_ms: i64,
    ) -> Result<(), AppErrorFfi> {
        ffi_runtime().block_on(async {
            let ws = self.ws.lock().expect("ws mutex poisoned").clone();
            let mut inner = self.inner.lock().await;
            let body = String::from_utf8_lossy(&plaintext).into_owned();
            let profile_key = inner.own_profile_key().await;
            let msg = ContentMessage {
                body: Some(Body::Text(TextMessage { body })),
                timestamp_ms: sent_at_ms as u64,
                profile_key,
            };
            inner.send_dm(ws.as_ref(), &recipient_did, &msg.encode_to_vec(), None).await?;
            // Sending a DM is a deliberate gesture (docs/35 §"What changes a row").
            let _ = inner
                .store
                .touch_contact(&recipient_did, true, Timestamp::now())
                .await;
            Ok::<_, AppError>(())
        }).map_err(AppErrorFfi::from)
    }

