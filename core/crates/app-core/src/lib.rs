fn summary_to_ffi(s: groups::GroupSummary) -> GroupSummaryFfi {
    GroupSummaryFfi {
        group_id: s.group_id,
        master_key: s.master_key,
        revision: s.revision,
        title: s.title,
        description: s.description,
        expiry_seconds: s.expiry_seconds,
        members: s
            .members
            .into_iter()
            .map(|m| GroupMemberFfi {
                did: m.did,
                encrypted_member_id: m.encrypted_member_id,
                role: m.role,
                joined_at_ms: m.joined_at_ms,
            })
            .collect(),
        pending_invites: s
            .pending_invites
            .into_iter()
            .map(|p| GroupPendingFfi {
                encrypted_member_id: p.encrypted_member_id,
                timestamp_ms: p.timestamp_ms,
            })
            .collect(),
        pending_approvals: s
            .pending_approvals
            .into_iter()
            .map(|p| GroupPendingFfi {
                encrypted_member_id: p.encrypted_member_id,
                timestamp_ms: p.timestamp_ms,
            })
            .collect(),
    }
}

