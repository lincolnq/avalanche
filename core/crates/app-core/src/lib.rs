    /// Cheap snapshot of current connection state. Non-blocking.
    pub fn connection_state(&self) -> ConnectionState {
        self.state_tx.borrow().clone()
    }

    /// Block until the connection state differs from `last`, then return the
    /// new value. iOS runs this in a tight loop on a dedicated task and
    /// publishes to its UI state.
    ///
    /// Returns `Err` if the underlying watch sender is dropped (i.e. the
    /// `AppCore` is being torn down).
    pub fn wait_for_connection_state_change(
        &self,
        last: ConnectionState,
    ) -> Result<ConnectionState, AppErrorFfi> {
        ffi_runtime().block_on(async {
            let mut rx = self.state_tx.subscribe();
            let current = rx.borrow().clone();
            if current != last {
                return Ok::<_, AppError>(current);
            }
            rx.changed()
                .await
                .map_err(|_| AppError::Protocol("connection state channel closed".into()))?;
            let new_state = rx.borrow().clone();
            Ok(new_state)
        })
        .map_err(AppErrorFfi::from)
    }

    /// Block until at least one event is available; drain the queue and
    /// return the batch.
    ///
    /// Single-consumer: concurrent FFI callers serialize on the receiver
    /// mutex. Returns `Err` if the event channel is closed (`AppCore` torn
    /// down).
    pub fn next_events(&self) -> Result<Vec<IncomingEvent>, AppErrorFfi> {
        ffi_runtime().block_on(async {
            let mut rx = self.event_rx.lock().await;
            let first = rx
                .recv()
                .await
                .ok_or_else(|| AppError::Protocol("event channel closed".into()))?;
            let mut batch = vec![first];
            while let Ok(more) = rx.try_recv() {
                batch.push(more);
            }
            Ok::<_, AppError>(batch)
        })
        .map_err(AppErrorFfi::from)
    }

    /// Save a message to local history (SQLCipher).
    pub fn save_message(&self, msg: StoredMessageFfi) -> Result<(), AppErrorFfi> {
        let timed = msg.expire_timer_secs > 0;
        ffi_runtime().block_on(async {
            let inner = self.inner.lock().await;
            inner.store.save_message(&store::messages::HistoryMessage {
                id: msg.id,
                conversation_id: msg.conversation_id,
                sender_did: msg.sender_did,
                body: msg.body,
                sent_at: Timestamp(msg.sent_at_ms),
                edited_at: msg.edited_at_ms.map(Timestamp),
                read_at: msg.read_at_ms.map(Timestamp),
                delivery_status: msg.delivery_status,
                // Owned by apply_edit / tombstone_message; save_message's SQL
                // preserves the stored values via ON CONFLICT, so these are
                // placeholders that are never written for an existing row.
                edit_count: 0,
                deleted_at: None,
                // Normal chat message. System/metadata rows are written by
                // the group-event path, never through this FFI.
                kind: 0,
                metadata: None,
                // Disappearing-messages timer stamped by the sender (docs/03 §5);
                // the store computes `expire_at` from `read_at`. `expire_at` is
                // store-owned (set on read), so it's a placeholder here.
                expire_timer_secs: msg.expire_timer_secs as i64,
                expire_at: None,
            }).await.map_err(AppError::from)
        }).map_err(AppErrorFfi::from)?;
        // Wake the reaper so it schedules this message's deadline (docs/03 §5).
        if timed {
            self.expire_notify.notify_one();
        }
        Ok(())
    }

