    /// Async send_read_receipt for use in tests and the testbot.
    /// Fans out across all of the recipient's active devices.
    pub async fn send_read_receipt_async(
        &self,
        recipient_did: &str,
        timestamps: Vec<i64>,
    ) -> Result<(), AppError> {
        let ws = self.ws.lock().expect("ws mutex poisoned").clone();
        let mut inner = self.inner.lock().await;
        // Mirror the FFI path: no read receipts to an un-accepted sender
        // (message-request gate, docs/12 §1).
        let curated = inner
            .store
            .load_contact(recipient_did)
            .await
            .ok()
            .flatten()
            .map(|c| c.is_curated)
            .unwrap_or(false);
        if !curated {
            return Ok(());
        }
        let profile_key = inner.own_profile_key().await;
        let msg = ContentMessage {
            body: Some(Body::Receipt(ReceiptMessage {
                r#type: receipt_message::Type::Read as i32,
                timestamps: timestamps.into_iter().map(|t| t as u64).collect(),
            })),
            timestamp_ms: 0,
            profile_key,
            expire_timer_secs: 0,
        };
        inner.send_dm(ws.as_ref(), recipient_did, &msg.encode_to_vec(), None).await
    }

