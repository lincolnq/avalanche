    /// Async variant of `create_group` (for tests / testbot). Seeds the
    /// founder's own Sender Key locally on success.
    pub async fn create_group_async(
        &self,
        title: &str,
        description: &str,
        expiry_seconds: u32,
    ) -> Result<groups::CreatedGroup, AppError> {
        let ws = self.ws.lock().expect("ws mutex poisoned").clone();
        let mut inner = self.inner.lock().await;
        let did = inner.did.clone();
        let device_id = inner.device_id;
        let server_url = inner.client.server_url().to_string();
        let created = groups::create_group(
            &inner.store,
            &inner.client,
            &server_url,
            &did,
            title,
            description,
            expiry_seconds,
        )
        .await?;
        let mk: [u8; 32] = created
            .master_key
            .clone()
            .try_into()
            .map_err(|_| AppError::Protocol("master_key length != 32".into()))?;
        let _ = groups::seed_own_sender_key(&mut inner.store, &did, device_id, &mk).await?;
        // Refresh the server-side recovery blob with this new group so a
        // recovered device can rejoin without help from another member.
        inner.refresh_recovery_blob_best_effort().await;
        // Subscribe to the founder's group_push_pseudonym so replies from
        // invitees push live. Mirrors the FFI `create_group` wiring.
        if let (Some(ws), Some(pseudonym)) = (
            ws.as_ref(),
            inner
                .store
                .load_group(&created.group_id)
                .await
                .ok()
                .flatten()
                .and_then(|g| g.group_push_pseudonym),
        ) {
            if let Err(e) = ws.subscribe_group_pseudonyms(vec![pseudonym]) {
                tracing::warn!(
                    "[groups] subscribe to new group pseudonym for {} failed: {e}",
                    created.group_id
                );
            }
        }
        Ok(created)
    }

