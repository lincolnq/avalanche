    /// Enumerate every conversation that has at least one persisted message,
    /// with that conversation's most recent message attached. One row per
    /// conversation, sorted newest-first. The mobile chat list is derived
    /// directly from this — no parallel persistence in UserDefaults.
    pub fn load_conversations(&self) -> Result<Vec<ConversationSummaryFfi>, AppErrorFfi> {
        ffi_runtime().block_on(async {
            let inner = self.inner.lock().await;
            let rows = inner.store.load_conversations(Timestamp::now()).await
                .map_err(AppError::from)?;
            // Resolve all group titles from locally-persisted state in a single
            // query, so the chat list renders real names on launch without a
            // per-group round trip.
            let titles = groups::local_group_titles(&inner.store).await?;
            // Index contacts by DID once so per-DM request/blocked flags resolve
            // without a query per row (docs/12 §1–2). The DM peer is the tail of
            // the `dm-{self}-{peer}` conversation id.
            let dm_prefix = format!("dm-{}-", inner.did);
            let contacts: std::collections::HashMap<String, store::contacts::ContactRow> = inner
                .store
                .list_contacts()
                .await
                .map_err(AppError::from)?
                .into_iter()
                .map(|c| (c.did.clone(), c))
                .collect();
            // Homeserver-known bots are never message requests (docs/12). The
            // inbound gate (`SenderGate::passes`) already exempts them via
            // `is_bot`, so this load path must too — otherwise an auto-accepted
            // bot DM (e.g. adminbot's welcome) flips to "message request" on the
            // next launch, when the conversation is rebuilt from the store.
            let bot_dids: std::collections::HashSet<String> = inner
                .store
                .list_bot_dids()
                .await
                .map_err(AppError::from)?
                .into_iter()
                .collect();
            Ok::<_, AppError>(rows.into_iter().map(|c| {
                let peer = c.conversation_id.strip_prefix(&dm_prefix);
                let contact = peer.and_then(|p| contacts.get(p));
                let is_blocked = contact.map(|c| c.is_blocked).unwrap_or(false);
                // Defer the request/blocked decision to the same `SenderGate`
                // the inbound delivery path uses, so the two can't drift (see
                // SenderGate::is_request). Non-DM rows (peer == None) are never
                // requests.
                let is_request = peer.is_some_and(|p| {
                    crate::messaging::SenderGate {
                        is_curated: contact.map(|c| c.is_curated).unwrap_or(false),
                        is_blocked,
                        is_bot: bot_dids.contains(p),
                    }
                    .is_request()
                });
                ConversationSummaryFfi {
                    group_title: c.conversation_id
                        .strip_prefix("group-")
                        .and_then(|gid| titles.get(gid).cloned()),
                    conversation_id: c.conversation_id,
                    last_message: c.last_message.map(stored_to_ffi),
                    is_request,
                    is_blocked,
                }
            }).collect())
        }).map_err(AppErrorFfi::from)
    }

